const { Model, DataTypes } = require( 'sequelize' );
const sequelize = require( '../config/connections' );

class Product extends Model {}

Product.init(
	{
		id: {
			type: DataTypes.INTEGER,
			allowNull: false,
			primaryKey: true,
			autoIncrement: true,
		},
		product_name: {
			type: DataTypes.STRING,
			allowNull: false,
		},
		date_purchased: {
			type: DataTypes.DATEONLY,
			defaultValue: DataTypes.NOW,
			allowNull: false,
		},
		expiration_date: {
			type: DataTypes.DATEONLY,
			allowNull: true,
		},
		user_id: {
			type: DataTypes.INTEGER,
			references: {
				model: 'user',
				key: 'id',
			},
		},
		category_id: {
			type: DataTypes.INTEGER,
			references: {
				model: 'category',
				key: 'id',
			},
		}
	},
	{
		sequelize,
		timestamps: false,
		freezeTableName: true,
		underscored: true,
		modelName: 'product',
	}
);

module.exports = Product;